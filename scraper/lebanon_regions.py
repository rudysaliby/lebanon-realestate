def lookup(name: str) -> dict | None:
    """
    Resolve any Lebanese area/neighborhood name to:
    {"area": str|None, "subregion": str, "region": str, "lat": float, "lng": float}
    Returns None if not found.
    """
    if not name:
        return None
    key = _norm(name)
    if key in AREA_LOOKUP:
        return AREA_LOOKUP[key]
    aliased = ALIASES.get(key)
    if aliased:
        r = AREA_LOOKUP.get(_norm(aliased))
        if r:
            return r
        sr = SUBREGION_LOOKUP.get(_norm(aliased))
        if sr:
            return {**sr, "area": None}
    if key in SUBREGION_LOOKUP:
        return {**SUBREGION_LOOKUP[key], "area": None}
    return None


def lookup_region(name: str) -> tuple[str, str] | None:
    """Legacy helper: returns (region, subregion) or None."""
    r = lookup(name)
    return (r["region"], r["subregion"]) if r else None


# ── Dynamic cache (grows as Google Maps geocodes new areas) ───────────────────
import os as _os, json as _json

_CACHE_FILE = _os.path.join(_os.path.dirname(__file__), "regions_cache.json")

def _load_cache() -> dict:
    try:
        with open(_CACHE_FILE) as f:
            return _json.load(f)
    except:
        return {}

def _save_cache(cache: dict):
    try:
        with open(_CACHE_FILE, "w") as f:
            _json.dump(cache, f, indent=2, ensure_ascii=False)
    except:
        pass

def cache_lookup(area_name: str) -> dict | None:
    """Check dynamic cache for areas geocoded by Google Maps."""
    cache = _load_cache()
    key = _norm(area_name)
    return cache.get(key)

def cache_save(area_name: str, lat: float, lng: float, region: str = None, subregion: str = None):
    """
    Save a newly geocoded area permanently:
    1. Saves to regions_cache.json (instant lookup next run)
    2. Inserts into lebanon_regions.py REGIONS dict (regex scan picks it up)
    """
    import re as _re

    # 1. Save to cache
    cache = _load_cache()
    key = _norm(area_name)
    cache[key] = {
        "area": area_name,
        "lat": round(lat, 6),
        "lng": round(lng, 6),
        "region": region or "",
        "subregion": subregion or "",
    }
    _save_cache(cache)

    # 2. Insert into lebanon_regions.py REGIONS dict
    # Determine region/subregion bucket — default to "Other > Other"
    region_key    = region or "Other"
    subregion_key = subregion or "Other"

    try:
        regions_file = _os.path.join(_os.path.dirname(__file__), "lebanon_regions.py")
        with open(regions_file, "r", encoding="utf-8") as f:
            file_content = f.read()

        # Check if area already exists
        escaped = _re.escape(area_name)
        if _re.search(rf"['\"]{escaped}['\"]\s*:", file_content):
            return  # Already in file

        # Find the subregion bucket and insert the new area
        # Pattern: look for the subregion dict and insert before its closing }
        subr_escaped = _re.escape(subregion_key)
        pattern = rf"('{subr_escaped}'\s*:\s*\{{[^}}]*?)(\}})"
        new_entry = f"    '{area_name}': ({round(lat, 6)}, {round(lng, 6)}),\n        "
        replacement = r"\1" + new_entry + r"\2"
        new_content, n = _re.subn(pattern, replacement, file_content, count=1, flags=_re.DOTALL)

        if n == 0:
            # Subregion not found — try to add to region
            region_escaped = _re.escape(region_key)
            pattern2 = rf"('{region_escaped}'\s*:\s*\{{)"
            new_subregion = f"\n        '{subregion_key}': {{\n            '{area_name}': ({round(lat, 6)}, {round(lng, 6)}),\n        }},"
            replacement2 = r"\1" + new_subregion
            new_content, n2 = _re.subn(pattern2, replacement2, file_content, count=1)
            if n2 == 0:
                print(f"  [Cache] Saved to cache only (region '{region_key}' not found in file): {area_name}")
                return

        with open(regions_file, "w", encoding="utf-8") as f:
            f.write(new_content)

        print(f"  [Regions] Added '{area_name}' to lebanon_regions.py ({lat:.4f}, {lng:.4f})")

    except Exception as e:
        print(f"  [Cache] Saved to cache (file update failed: {e}): {area_name}")